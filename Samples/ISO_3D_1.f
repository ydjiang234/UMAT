      SUBROUTINE UMAT(STRESS,STATEV,DDSDDE,SSE,SPD,SCD,
     1 RPL,DDSDDT,DRPLDE,DRPLDT,STRAN,DSTRAN,
     2 TIME,DTIME,TEMP,DTEMP,PREDEF,DPRED,CMNAME,NDI,NSHR,NTENS,
     3 NSTATV,PROPS,NPROPS,COORDS,DROT,PNEWDT,CELENT,
     4 DFGRD0,DFGRD1,NOEL,NPT,LAYER,KSPT,JSTEP,KINC)
C
      INCLUDE 'ABA_PARAM.INC'
C
      CHARACTER*80 CMNAME
      DIMENSION STRESS(NTENS),STATEV(NSTATV),
     1 DDSDDE(NTENS,NTENS),DDSDDT(NTENS),DRPLDE(NTENS),
     2 STRAN(NTENS),DSTRAN(NTENS),TIME(2),PREDEF(1),DPRED(1),
     3 PROPS(NPROPS),COORDS(3),DROT(3,3),DFGRD0(3,3),DFGRD1(3,3),
     4 JSTEP(4)
C    Start of the code
      PARAMETER (ZERO=0.0D0,ONE=1.0D0,TWO=2.0D0)
      E=PROPS(1)
      ANU=PROPS(2)
      PARA1=E/(ONE+ANU)/(ONE-TWO*ANU)
C
      DO I=1,NTENS
        DO J=1,NTENS
        DDSDDE(I,J)=(ZERO)
        ENDDO
      ENDDO
C
      DDSDDE(1,1)=(PARA1*(ONE-ANU))
      DDSDDE(2,2)=(PARA1*(ONE-ANU))
      DDSDDE(3,3)=(PARA1*(ONE-ANU))
      DDSDDE(4,4)=(PARA1*(ONE-TWO*ANU))
      DDSDDE(5,5)=(PARA1*(ONE-TWO*ANU))
      DDSDDE(6,6)=(PARA1*(ONE-TWO*ANU))
      DDSDDE(1,2)=(PARA1*ANU)
      DDSDDE(2,1)=(PARA1*ANU)
      DDSDDE(1,3)=(PARA1*ANU)
      DDSDDE(3,1)=(PARA1*ANU)
      DDSDDE(2,3)=(PARA1*ANU)
      DDSDDE(3,2)=(PARA1*ANU)
C     
      DO I=1,NTENS
        DO J=1,NTENS
        STRESS(I)=STRESS(I)+DDSDDE(I,J)*DSTRAN(J)
        ENDDO
      ENDDO
      RETURN
      END
